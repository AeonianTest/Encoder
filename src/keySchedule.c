typedef struct roundKey roundKey;
roundKey *RKarray[11]; //11 unique pointers to Roundkeys assigned memory, globally

void keySchedule(char *keyPhrase) {

    for (int i = 0; i < 11; ++i) //assign memory to global pointer structs
        RKarray[i] = malloc(sizeof(roundKey)); //TODO: make sure to FREE LATER and clean up memory <<<<<<<<<<

    for (int i = 0; i < 4; ++i) { //Read the first keyphrase into the first, get k0
        RKarray[0]->word0[i] = *(keyPhrase + i);
        RKarray[0]->word1[i] = *(keyPhrase + 4 + i);
        RKarray[0]->word2[i] = *(keyPhrase + 8 + i);
        RKarray[0]->word3[i] = *(keyPhrase + 12 + i);
    } memcpy(RKarray[0]->fullString, keyPhrase, strlen(keyPhrase));

    for (int i = 1; i < 11; ++i) { //Loop 10 times to genrate keys k1 - k10 from k0
        rotWord(RKarray[i], RKarray[i - 1]); //3ops
        subWord(RKarray[i]);
        rCon(RKarray[i]);
        
        fillRK(RKarray[i], RKarray[i - 1]);
    }

    return;
}

void fillRK(roundKey *keyCurrent, roundKey *keyPrevious) {
    for (int i = 0; i < 4; ++i) { //Generate each row of the 4 words by XOR it with corresponding word in prev ^ previous word.
        keyCurrent->word0[i] ^= keyPrevious->word0[i]; //ALL THIS NEEDS TO BE TESTED STILL
        keyCurrent->word1[i] = keyPrevious->word1[i] ^ keyCurrent->word0[i];
        keyCurrent->word2[i] = keyPrevious->word2[i] ^ keyCurrent->word1[i];
        keyCurrent->word3[i] = keyPrevious->word3[i] ^ keyCurrent->word2[i];
    }

    return;
}

